#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)dest;
	s2 = (unsigned char *)src;
	i = -1;
	if (!s1 && !s2)
		return (dest);
	while (++i < n)
		s1[i] = s2[i];
	return (dest);
}
